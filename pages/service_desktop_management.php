    <script>
        // Sidebar Toggle
        const sidebarToggle = document.getElementById('sidebarToggle');
        const sidebar = document.getElementById('sidebar');
        const sidebarOverlay = document.getElementById('sidebarOverlay');

        sidebarToggle.addEventListener('click', () => {
            sidebar.classList.toggle('-translate-x-full');
            sidebarOverlay.classList.toggle('hidden');
        });

        sidebarOverlay.addEventListener('click', () => {
            sidebar.classList.add('-translate-x-full');
            sidebarOverlay.classList.add('hidden');
        });

        // Modal Management
        const addServiceModal = document.getElementById('addServiceModal');
        const addServiceBtn = document.getElementById('addServiceBtn');
        const closeAddServiceModal = document.getElementById('closeAddServiceModal');
        const cancelAddService = document.getElementById('cancelAddService');
        const confirmationModal = document.getElementById('confirmationModal');

        addServiceBtn.addEventListener('click', () => {
            addServiceModal.classList.remove('hidden');
        });

        [closeAddServiceModal, cancelAddService].forEach(btn => {
            btn.addEventListener('click', () => {
                addServiceModal.classList.add('hidden');
            });
        });

        // Add Service Form
        document.getElementById('addServiceForm').addEventListener('submit', (e) => {
            e.preventDefault();
            // Simulate adding service
            addServiceModal.classList.add('hidden');
            showNotification('Service added successfully!', 'success');
        });

        // Drag and Drop Functionality
        let draggedElement = null;
        let pendingAssignment = null;

        // Make services draggable
        const serviceItems = document.querySelectorAll('.service-item');
        serviceItems.forEach(item => {
            item.addEventListener('dragstart', (e) => {
                draggedElement = e.target;
                e.target.style.opacity = '0.5';
            });

            item.addEventListener('dragend', (e) => {
                e.target.style.opacity = '1';
                draggedElement = null;
            });
        });

        // Make desktop drop zones
        const dropZones = document.querySelectorAll('.drop-zone');
        dropZones.forEach(zone => {
            zone.addEventListener('dragover', (e) => {
                e.preventDefault();
                zone.classList.add('bg-primary-100', 'border-2', 'border-dashed', 'border-primary-300');
            });

            zone.addEventListener('dragleave', (e) => {
                zone.classList.remove('bg-primary-100', 'border-2', 'border-dashed', 'border-primary-300');
            });

            zone.addEventListener('drop', (e) => {
                e.preventDefault();
                zone.classList.remove('bg-primary-100', 'border-2', 'border-dashed', 'border-primary-300');
                
                if (draggedElement) {
                    const serviceId = draggedElement.dataset.serviceId;
                    const desktopId = zone.dataset.desktop;
                    
                    pendingAssignment = {
                        serviceId,
                        desktopId,
                        serviceName: draggedElement.querySelector('h4').textContent,
                        desktopName: `Desktop ${desktopId}`
                    };
                    
                    confirmationModal.classList.remove('hidden');
                }
            });
        });

        // Confirmation Modal
        document.getElementById('cancelConfirmation').addEventListener('click', () => {
            confirmationModal.classList.add('hidden');
            pendingAssignment = null;
        });

        document.getElementById('confirmAssignment').addEventListener('click', () => {
            if (pendingAssignment) {
                // Simulate assignment
                const { serviceId, desktopId, serviceName } = pendingAssignment;
                
                // Add visual feedback
                const dropZone = document.querySelector(`[data-desktop="${desktopId}"]`);
                const existingPlaceholder = dropZone.querySelector('.text-center');
                if (existingPlaceholder) {
                    existingPlaceholder.remove();
                }
                
                // Create assigned service element
                const assignedService = document.createElement('div');
                assignedService.className = 'assigned-service bg-primary-100 text-primary-600 px-3 py-1 rounded-md text-sm flex items-center justify-between';
                assignedService.innerHTML = `
                    <span>${serviceName}</span>
                    <button class="text-primary-400 hover:text-primary-600">
                        <i class="fas fa-times text-xs"></i>
                    </button>
                `;
                
                dropZone.appendChild(assignedService);
                
                confirmationModal.classList.add('hidden');
                showNotification(`${serviceName} assigned to Desktop ${desktopId}`, 'success');
                pendingAssignment = null;
            }
        });

        // Remove service assignment
        document.addEventListener('click', (e) => {
            if (e.target.closest('.assigned-service button')) {
                const serviceElement = e.target.closest('.assigned-service');
                const serviceName = serviceElement.querySelector('span').textContent;
                serviceElement.remove();
                showNotification(`${serviceName} removed from assignment`, 'info');
            }
        });

        // Bulk Assignment
        document.getElementById('bulkAssignBtn').addEventListener('click', () => {
            showNotification('Bulk assignment feature coming soon!', 'info');
        });

        // Notification System
        function showNotification(message, type = 'info') {
            const notification = document.createElement('div');
            notification.className = `fixed top-4 right-4 z-50 p-4 rounded-lg shadow-lg transition-smooth ${
                type === 'success' ? 'bg-success text-white' :
                type === 'error' ? 'bg-error text-white' :
                'bg-primary text-white'
            }`;
            notification.innerHTML = `
                <div class="flex items-center space-x-2">
                    <i class="fas fa-${type === 'success' ? 'check' : type === 'error' ? 'exclamation-triangle' : 'info-circle'}"></i>
                    <span>${message}</span>
                </div>
            `;
            
            document.body.appendChild(notification);
            
            setTimeout(() => {
                notification.style.opacity = '0';
                setTimeout(() => {
                    document.body.removeChild(notification);
                }, 300);
            }, 3000);
        }

        // Real-time updates simulation
        setInterval(() => {
            // Simulate queue updates
            const queueElements = document.querySelectorAll('.desktop-card');
            queueElements.forEach(desktop => {
                const queueSpan = desktop.querySelector('.text-xs span');
                if (queueSpan && queueSpan.textContent.includes('Queue:')) {
                    const currentQueue = parseInt(queueSpan.textContent.match(/\d+/)?.[0] || 0);
                    const variation = Math.floor(Math.random() * 3) - 1; // -1, 0, or 1
                    const newQueue = Math.max(0, currentQueue + variation);
                    queueSpan.textContent = `Queue: ${newQueue} tickets`;
                }
            });
        }, 10000);

        // Initialize sortable for services list
        new Sortable(document.getElementById('servicesList'), {
            animation: 150,
            ghostClass: 'opacity-50',
            chosenClass: 'bg-primary-100',
            handle: '.fa-grip-vertical'
        });
    </script>
